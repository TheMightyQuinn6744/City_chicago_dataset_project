SELECT CASE_NUMBER AS "Case Number", PRIMARY_TYPE AS "Crime Type", COMMUNITY_AREA_NAME AS "Community Name", LOCATION_DESCRIPTION AS "Location"
FROM CHICAGO_CRIME_DATA 
INNER JOIN CHICAGO_PUBLIC_SCHOOLS ON CHICAGO_PUBLIC_SCHOOLS.COMMUNITY_AREA_NUMBER = CHICAGO_CRIME_DATA.COMMUNITY_AREA_NUMBER
WHERE LOCATION_DESCRIPTION LIKE '%SCHOOL%';
